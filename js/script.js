const reverseDict = {};
for (let key in dictionary) {
    reverseDict[dictionary[key]] = key;
}

let currentLang = localStorage.getItem('site_lang') || 'bn';

document.addEventListener("DOMContentLoaded", () => {
    const langSwitchList = document.querySelectorAll('.lang-switch');
    
    if(currentLang === 'en') {
        applyTranslation(dictionary);
        langSwitchList.forEach(btn => btn.innerText = "বাংলা");
    }

    langSwitchList.forEach(btn => {
        btn.style.cursor = 'pointer';
        btn.addEventListener('click', () => {
            if (currentLang === 'bn') {
                currentLang = 'en';
                applyTranslation(dictionary);
                langSwitchList.forEach(b => b.innerText = "বাংলা");
                localStorage.setItem('site_lang', 'en');
            } else {
                currentLang = 'bn';
                applyTranslation(reverseDict);
                langSwitchList.forEach(b => b.innerText = "English");
                localStorage.setItem('site_lang', 'bn');
            }
        });
    });
});

function applyTranslation(mapDict) {
    const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, null, false);
    let node;
    while (node = walker.nextNode()) {
        let text = node.nodeValue.trim();
        if (text && mapDict[text]) {
            node.nodeValue = node.nodeValue.replace(text, mapDict[text]);
        }
    }
}
